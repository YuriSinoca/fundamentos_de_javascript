let x = 10
let y = 20

//Maior que 
console.log(x > y)

//Menor que
console.log(x < y)

//Maior ou igual a
console.log(x >= y)

//Menor ou igual a 
console.log(x <= y)

//Igual 
console.log(10 == "10")

//Igualdade estrita
console.log(10 === "10")

//Diferente de 
console.log(10 != 20)

